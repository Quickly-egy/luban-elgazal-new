export const getBlogById = (id) => {
  return blogData.find(blog => blog.id === parseInt(id));
};

export const getBlogsByCategory = (category) => {
  if (category === 'الكل') return blogData;
  return blogData.filter(blog => blog.category === category);
};

export const getRelatedBlogs = (currentId, category, limit = 3) => {
  return blogData
    .filter(blog => blog.id !== parseInt(currentId) && blog.category === category)
    .slice(0, limit);
}; 